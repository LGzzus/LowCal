package com.example.lowca.model;

public class DatosAnt {
    String genero;
    int estatura;
    float pesoO;
    float pesoA;
    String fechaN;
    String actividadF;

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public int getEstatura() {
        return estatura;
    }

    public void setEstatura(int estatura) {
        this.estatura = estatura;
    }

    public float getPesoO() {
        return pesoO;
    }

    public void setPesoO(float pesoO) {
        this.pesoO = pesoO;
    }

    public float getPesoA() {
        return pesoA;
    }

    public void setPesoA(float pesoA) {
        this.pesoA = pesoA;
    }

    public String getFechaN() {
        return fechaN;
    }

    public void setFechaN(String fechaN) {
        this.fechaN = fechaN;
    }

    public String getActividadF() {
        return actividadF;
    }

    public void setActividadF(String actividadF) {
        this.actividadF = actividadF;
    }
}
